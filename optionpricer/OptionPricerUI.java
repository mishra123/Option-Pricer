    public void updateAlgorithmCombobox() {
        Map<String,Algorithm> algorithms = AlgorithmBusiness.getCurrentAlgorithm();
        AlgorithmComboBox.removeAllItems();
        Set<String> keys = algorithms.keySet();
        for (String key : keys) {
            AlgorithmComboBox.addItem(key);
        }
        if (AlgorithmComboBox.getItemCount() > 0) AlgorithmComboBox.setSelectedIndex(0);
    }
    
