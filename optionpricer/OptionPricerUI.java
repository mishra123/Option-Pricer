    private void AlgorithmComboBoxFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_AlgorithmComboBoxFocusGained
        // TODO add your handling code here:
        if (option == Option.ASIAN){      
         this.AlgorithmComboBox.removeItem("Bionomial Tree");
         boolean allAlgo = false;
            for(int i= 0; i<this.AlgorithmComboBox.getItemCount(); i++){
                System.out.println(i);
                if(this.AlgorithmComboBox.getItemAt(i) == "Simulation"){
                    allAlgo = true;
                }
            }
            if (allAlgo == false){
                this.AlgorithmComboBox.addItem("Simulation");
            }}
        else{
            boolean allAlgo = false;
            for(int i= 0; i<this.AlgorithmComboBox.getItemCount(); i++){
                System.out.println(i);
                if(this.AlgorithmComboBox.getItemAt(i) == "Bionomial Tree"){
                    allAlgo = true;
                }
            }
            if (allAlgo == false){
                this.AlgorithmComboBox.addItem("Bionomial Tree");
            }
        }
    }//GEN-LAST:event_AlgorithmComboBoxFocusGained

